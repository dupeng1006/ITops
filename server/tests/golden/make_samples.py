# -*- coding: utf-8 -*-
"""
M1 黄金样本构造脚本（全脱敏虚构数据）

重要声明：
    本脚本生成的所有数据均为虚构的脱敏测试数据，不包含任何真实生产数据。
    产品名称一律使用"测试产品A01"等虚构名，金额均为虚构数值，
    信托计划代码仅借用规则配置中的代码形态（如 7001->6001 映射、
    AZ0206/AZ0205 大宗代码）用于验证核对逻辑，不指向任何真实产品。

样本设计（覆盖 11 步核对流水线的关键分支）：
    a. 精确匹配且差异 ≤1%（正常无色）        —— 测试产品A01 等
    b. 精确匹配且差异 >1%（浅红 FFCCCC）     —— 测试产品A02
    c. 映射场景：净值表 7001 映射为 6001      —— 测试资本金
    d. 映射去重冲突：原始 6001 + 映射 6001    —— 删除"测试公司资金"，保留"测试资本金"
    e. I9 过滤：同代码一含 I9 一不含          —— 测试稳健A04（保留非 I9）；
       同代码全含 I9（全部保留）              —— 测试全I9产品A05（精确匹配 1 对 2）
    f. 大宗产品：AZ0206、AZ0205              —— 标橙 FFC000、备注"大宗产品无需核对"
    g. 模糊匹配：测试稳益11号 vs 测试稳益11号A类 —— 相似度 0.875 ≥ 0.5
    h. 未匹配：测试孤儿A09                    —— 浅黄 FFFF99
    i. 代码格式陷阱：净值表 6101 为数值型      —— 验证 astype(str) 路径

输出：
    samples/golden/基金资产表_样本.xlsx  （28 列 × 15 行）
    samples/golden/净值查询表_样本.xlsx  （9 列 × 17 行）

作者：技术部
版本：1.0.0
日期：2026-07-17
"""

import sys
from pathlib import Path

import pandas as pd

# 项目根目录（本文件位于 server/tests/golden/make_samples.py，parents[3] 即项目根）
PROJECT_ROOT = Path(__file__).resolve().parents[3]
SAMPLE_DIR = PROJECT_ROOT / "samples" / "golden"

FUND_SAMPLE_PATH = SAMPLE_DIR / "基金资产表_样本.xlsx"
NETVALUE_SAMPLE_PATH = SAMPLE_DIR / "净值查询表_样本.xlsx"

# =============================================================================
# 虚构样本数据定义
# =============================================================================

# 基金资产表关键列（按位置）：2=组合编号, 3=组合名称, 11=基金资产净值, 19=信托计划代码, 27=基金总资产
# 每行: (组合编号, 组合名称, 基金资产净值, 信托计划代码, 基金总资产)
FUND_ROWS = [
    ("ZH001", "测试产品A01",      99_200_000.00, "C1001",  100_000_000.00),  # a 正常无色
    ("ZH002", "测试产品A02",      98_500_000.00, "C1002",  100_000_000.00),  # b 差异>1% 浅红
    ("ZH003", "测试资本金",       79_200_000.00, "6001",    80_300_000.00),  # c/d 映射+去重
    ("ZH004", "测试稳健A04",      59_700_000.00, "C1004",   60_200_000.00),  # e I9 过滤
    ("ZH005", "测试全I9产品A05",  29_950_000.00, "C1005",   30_050_000.00),  # e 全 I9 全保留(1对2)
    ("ZH006", "测试大宗A06",     194_000_000.00, "AZ0206", 200_000_000.00),  # f 大宗 橙
    ("ZH007", "测试大宗A07",     149_500_000.00, "AZ0205", 150_000_000.00),  # f 大宗 橙
    ("ZH008", "测试稳益11号",     39_900_000.00, "C1008",   40_200_000.00),  # g 模糊匹配
    ("ZH009", "测试孤儿A09",      10_000_000.00, "C1009",   10_500_000.00),  # h 未匹配 浅黄
    ("ZH010", "测试格式A10",      69_800_000.00, "6101",    70_000_000.00),  # i 格式陷阱
    ("ZH011", "测试产品A11",      20_100_000.00, "C1011",   20_300_000.00),  # a 正常无色
    ("ZH012", "测试产品A12",      25_200_000.00, "C1012",   25_500_000.00),  # a 正常无色
    ("ZH013", "测试产品A13",      18_050_000.00, "C1013",   18_200_000.00),  # a 正常无色
    ("ZH014", "测试产品A14",      33_100_000.00, "C1014",   33_400_000.00),  # a 正常无色
    ("ZH015", "测试产品A15",      44_200_000.00, "C1015",   44_600_000.00),  # a 正常无色
]

# 净值查询表关键列（按位置）：0=产品名称, 2=查询资产净值, 4=查询总资产, 8=信托计划代码
# 每行: (产品名称, 查询资产净值, 查询总资产, 信托计划代码)
NETVALUE_ROWS = [
    ("测试公司资金",           49_500_000.00,  50_000_000.00, "6001"),   # d 原始记录，应被映射去重删除
    ("测试资本金",             79_000_000.00,  80_000_000.00, "7001"),   # c 映射为 6001，应保留
    ("测试产品A01",            99_000_000.00, 100_500_000.00, "C1001"),  # a
    ("测试产品A02",            96_500_000.00,  98_000_000.00, "C1002"),  # b
    ("测试稳健A04-I9",            999_999.00,     999_999.00, "C1004"),  # e 含 I9，应被过滤
    ("测试稳健A04",            59_500_000.00,  60_000_000.00, "C1004"),  # e 非 I9，应保留
    ("测试全I9产品A05-I9份额", 29_800_000.00,  30_000_000.00, "C1005"),  # e 全含 I9，保留
    ("测试全I9产品A05-I9B类",  29_900_000.00,  30_100_000.00, "C1005"),  # e 全含 I9，保留
    ("测试大宗A06",           193_000_000.00, 195_000_000.00, "AZ0206"), # f
    ("测试大宗A07",           149_000_000.00, 150_500_000.00, "AZ0205"), # f
    ("测试稳益11号A类",        39_800_000.00,  40_000_000.00, "C8008"),  # g 模糊匹配候选
    ("测试格式A10",            69_500_000.00,  70_200_000.00, 6101),     # i 数值型代码（int）
    ("测试产品A11",            20_020_000.00,  20_200_000.00, "C1011"),  # a
    ("测试产品A12",            25_100_000.00,  25_400_000.00, "C1012"),  # a
    ("测试产品A13",            18_000_000.00,  18_150_000.00, "C1013"),  # a
    ("测试产品A14",            33_000_000.00,  33_300_000.00, "C1014"),  # a
    ("测试产品A15",            44_100_000.00,  44_500_000.00, "C1015"),  # a
]

FUND_COLUMNS = [f"列{i}" for i in range(28)]   # 基金资产表 28 列占位
NETVALUE_COLUMNS = [f"列{i}" for i in range(9)]  # 净值查询表 9 列占位


def build_fund_sample() -> pd.DataFrame:
    """构造基金资产表样本（28 列，关键列按位置填充，其余列空串占位）"""
    records = []
    for code_zh, name, nav, plan_code, total_asset in FUND_ROWS:
        row = [""] * 28
        row[2] = code_zh        # 组合编号
        row[3] = name           # 组合名称
        row[11] = nav           # 基金资产净值
        row[19] = plan_code     # 信托计划代码（字符串型）
        row[27] = total_asset   # 基金总资产
        records.append(row)
    return pd.DataFrame(records, columns=FUND_COLUMNS)


def build_netvalue_sample() -> pd.DataFrame:
    """构造净值查询表样本（9 列，关键列按位置填充；代码列混合格式：str + int）"""
    records = []
    for name, nav, total_asset, plan_code in NETVALUE_ROWS:
        row = [""] * 9
        row[0] = name           # 产品名称
        row[2] = nav            # 查询资产净值
        row[4] = total_asset    # 查询总资产
        row[8] = plan_code      # 信托计划代码（6101 行为 int，其余为 str）
        records.append(row)
    df = pd.DataFrame(records, columns=NETVALUE_COLUMNS)
    # 强制代码列为 object，保留 int/str 混合形态（模拟生产环境格式陷阱）
    df["列8"] = df["列8"].astype(object)
    return df


def main() -> int:
    SAMPLE_DIR.mkdir(parents=True, exist_ok=True)

    df_fund = build_fund_sample()
    df_fund.to_excel(FUND_SAMPLE_PATH, index=False)
    print(f"已生成: {FUND_SAMPLE_PATH}  ({df_fund.shape[0]}行 x {df_fund.shape[1]}列)")

    df_net = build_netvalue_sample()
    df_net.to_excel(NETVALUE_SAMPLE_PATH, index=False)
    print(f"已生成: {NETVALUE_SAMPLE_PATH}  ({df_net.shape[0]}行 x {df_net.shape[1]}列)")

    # 验证格式陷阱 i 已生效：读回后代码列应同时存在 int 与 str
    check = pd.read_excel(NETVALUE_SAMPLE_PATH, header=0)
    code_types = {type(v).__name__ for v in check["列8"]}
    print(f"净值表代码列读回类型集合: {sorted(code_types)}（预期含 int 与 str）")
    if "int" not in code_types:
        print("警告: 数值型代码陷阱未生效，请检查样本构造！", file=sys.stderr)
        return 1

    print("黄金样本构造完成（全脱敏虚构数据）")
    return 0


if __name__ == "__main__":
    sys.exit(main())
