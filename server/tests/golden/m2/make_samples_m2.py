from pathlib import Path

from openpyxl import Workbook

SAMPLES_DIR = Path(__file__).resolve().parent / "samples"

# 估值表列结构（第 4 行为列头，前 3 行为标题/产品名称/日期）
VALUATION_HEADERS = [
    "科目代码", "科目名称", "数量", "单位成本", "成本", "成本占净值%",
    "市价", "市值", "市值占净值%", "估值增值", "停牌信息",
]

# 系统端列结构（第 1 行起含列头）
SYSTEM_HEADERS = ["证券代码", "证券名称", "持仓", "估值价格", "市值"]


def make_system_file(path: Path, rows: list) -> None:
    wb = Workbook()
    ws = wb.active
    ws.title = "基金证券"
    ws.append(SYSTEM_HEADERS)
    for row in rows:
        ws.append(row)
    wb.save(path)
    print(f"已生成: {path.name}（{len(rows)} 行）")


def make_valuation_file(path: Path, product_name: str, rows: list) -> None:
    wb = Workbook()
    ws = wb.active
    ws.title = "估值表"
    ws.append(["证券投资基金估值表"])          # 第 1 行：标题
    ws.append([f"产品名称：{product_name}"])    # 第 2 行：产品名称
    ws.append(["估值日期：2026-07-20"])         # 第 3 行：日期
    ws.append(VALUATION_HEADERS)                # 第 4 行：列头
    for row in rows:
        ws.append(row)
    wb.save(path)
    print(f"已生成: {path.name}（明细 {len(rows)} 行 + 标题 3 行）")


